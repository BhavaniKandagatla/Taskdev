// Declare and initialize variables of different data types
var numberVar = 18; // Number
var stringVar = "Hello, world!"; // String
var booleanVar = true; // Boolean
var arrayVar = [1, 2, 3, 4, 5]; // Array
var objectVar = {name: "Bhavani", age: 19}; // Object
var nullVar = null; // Null
var undefinedVar; // Undefined

// Print variable values
console.log("Number Variable:", numberVar);
console.log("String Variable:", stringVar);
console.log("Boolean Variable:", booleanVar);
console.log("Array Variable:", arrayVar);
console.log("Object Variable:", objectVar);
console.log("Null Variable:", nullVar);
console.log("Undefined Variable:", undefinedVar);



